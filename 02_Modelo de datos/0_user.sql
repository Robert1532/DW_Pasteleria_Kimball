
DROP USER dmPasteleria CASCADE;
DROP USER stPasteleria CASCADE;

CREATE USER dmPasteleria IDENTIFIED BY dmPasteleria
  DEFAULT TABLESPACE users
  TEMPORARY TABLESPACE temp
  PROFILE DEFAULT
  ACCOUNT UNLOCK;

